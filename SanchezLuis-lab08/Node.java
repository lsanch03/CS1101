class Node {
  private int val ;
  private Node next;

  // CONSTRUCTORS
  public Node() {}

  // GETTERS
  public int getVal() {
    return val;
  }

  public Node getNext() {
    return next;
  }

  // SETTERS
  public void setVal(int val) {
    this.val = val;
  }

  public void setNext(Node next) {
    this.next = next;
  }
}
